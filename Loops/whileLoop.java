package Loops;

public class whileLoop {
    public static void main(String[] args) {
        int count = 1;
        while(count <= 5) {
            System.out.println("this is a while loop code");
            count++;
        }
    }
}

/* ok let's break down the syntax and will understand it in a simple manner
    syntax:
       int count = 1   --> initial value
       while (count <= 5)  --> condition
       count++  --> updation
*/ 
